	public int[][] getMatrixRed( int[][] inputArr ){
		int[][] resultArr = new int[8][3];
		
		if( inputArr.length != 8 || inputArr[0].length != 8 ){
			System.err.println(" Invalid input array!");
		}
		
		int ri = 0;
		
		//Convert the input array from the rightmost column.
		int[] thirdColumn = { 127, 191, 223, 239, 247, 251, 253, 254 };
		int thirdIndex = 0;
		for( int col = 7; col >= 0; col-- ){
			int colSum = 0;
			int powerOfTwo = 0;
			for( int row = 7; row >= 0; row-- ){
				colSum += inputArr[row][col] * Math.pow( 2, powerOfTwo );
				powerOfTwo++;
			}
			resultArr[ri][0] = colSum;
			resultArr[ri][1] = 0;
			resultArr[ri][2] = thirdColumn[thirdIndex];
			ri++;
			thirdIndex++;
		}
		
		return resultArr;
	}
	
